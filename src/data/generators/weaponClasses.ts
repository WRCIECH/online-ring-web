import type { WeaponClass, StatKey, Grade, DamageType, StatusType, AtomicOrigin } from '../../types/game'

type PoiseWeight = 'light' | 'medium' | 'heavy' | 'colossal'
import type { ContentProductType } from '../contentProducts'

export interface WeaponClassDef {
  id: WeaponClass
  name: string
  description: string
  poise_weight: PoiseWeight
  base_damage_mult: number
  supported_products: ContentProductType[]
  scaling: Partial<Record<StatKey, Grade>>
  // New fields from weapons_new.md
  base_damage_types: DamageType[]
  time_mod: number
  stamina_mod: number
  inherent_status?: StatusType
  // "transformation" candidate pool for drawTransformation() in weaponPatterns.ts.
  // Empty array disables drawTransformation for this class (mirrors how a
  // missing inherent_status disables drawEmotion).
  allowed_transformations: AtomicOrigin[]
  // Per-weapon cap on simultaneously-attached ContentItems (on top of the
  // global END-stat cap in gameStore.ts's selectEquipLoad).
  content_slots: number
  heavy_bonus_mult: number
  boss_rush_coeff: number
}

